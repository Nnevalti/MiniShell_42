#include <unistd.h>

void	ft_putnbr(int n)
{
	char			c;
	unsigned int	nbr;

	if (n < 0)
	{
		write(1, "-", 1);
		nbr = -n;
	}
	else
	{
		nbr = n;
	}
	if (nbr < 10)
	{
		c = nbr + '0';
		write(1, &c, 1);
	}
	else
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
}
